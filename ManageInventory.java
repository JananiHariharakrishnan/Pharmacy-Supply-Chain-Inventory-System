    private void DisplayInventory() 
    {
        try {
            String sql = "SELECT si.product_id, p.product_name, si.qty_supplied, p.unit_price " +
                        "FROM supplier_inventory si JOIN product1 p ON si.product_id = p.product_id WHERE si.supplier_id = ?";

            // Create a prepared statement with parameter placeholder
            PreparedStatement pstmt = con.prepareStatement(sql);
            //pstmt.setString(1, "S001"); //COMMENT THIS AND UNCOMMENT THE NEXT LINE AFTER DEBUGGING
            pstmt.setString(1, this.SupplierID);   

            // Print the SQL query for debugging purposes
            String sql1 = pstmt.toString();
            System.out.println("Executing query: " + this.SupplierID + sql1);

            DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();
            tblModel.setRowCount(0); // Clear existing rows
            
            // Execute the query
            ResultSet rs = pstmt.executeQuery();

            if (!rs.isBeforeFirst()) {
                JOptionPane.showMessageDialog(this, "No data found for the given criteria.");
            }

            while (rs.next()) 
            {
                String productID = rs.getString("product_id");
                String productName = rs.getString("product_name");
                int qtySupplied = rs.getInt("qty_supplied");
                float unitPrice = rs.getFloat("unit_price");

                // Create an object array to represent the row data
                Object[] row = {productID, productName, qtySupplied, unitPrice};
                //tblModel = (DefaultTableModel) jTable1.getModel();
                tblModel.addRow(row);           
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        } finally {
            try {
                // Close resources
                if (resultSet != null) resultSet.close();
                if (callableStatement != null) callableStatement.close();
                if (con != null) con.close();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }

